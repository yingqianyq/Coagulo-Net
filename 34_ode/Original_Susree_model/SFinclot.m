function dY = SFinclot(t,Y)

global C1 C2 C3 NDM TIM;

% C42=C2(6);

TIM = 1800;%690.071;%1800;

%Y = [TF; VII; TF:VII; VIIa; TF:VIIa; IX; IXa; IX(m); IXa(m); X; Xa; X(m); Xa(m);...
%     II; IIa; II(m); IIa(m); PL; AP; VIII; VIIIa; VIII(m); VIIIa(m);...
%     IXa:VIIIa(m); V; Va; V(m); Va(m); Xa:Va(m); I; Ia; TFPI; Xa:TFPI;
%     ATIII]; % U=34

NDM = [0.025; 10.0; 0.005; 10.0; 0.005; 90.0; 90.0; 10.0; 10.0; 170.0;...
       170.0; 10.0; 10.0; 1400.0; 1400.0; 10.0; 10.0; 10.0; 10.0;...
       0.7; 0.7; 0.7; 0.7; 0.7; 20.0; 20.0; 10.0; 10.0; 10.0; 7000.0;...
       7000.0; 2.5; 2.5; 3400.0];
   
dY = zeros(34,1);      

YD = Y.*NDM;

%TF = 5e-03; %nM;

dY(1)  = TIM*(- C1(1)*YD(1)*YD(2) + C1(2)*YD(3) - C1(3)*YD(1)*YD(4)...
         + C1(4)*YD(5))/NDM(1); %TF 
dY(2)  = TIM*(- C1(1)*YD(1)*YD(2) + C1(2)*YD(3) - C1(5)*YD(5)*YD(2)...
         - C1(6)*YD(11)*YD(2) - C1(7)*YD(15)*YD(2))/NDM(2); %VII
dY(3)  = TIM*(C1(1)*YD(1)*YD(2) - C1(2)*YD(3))/NDM(3); %TF:VII
dY(4)  = TIM*(- C1(3)*YD(1)*YD(4) + C1(4)*YD(5) + C1(5)*YD(5)*YD(2)...
         + C1(6)*YD(11)*YD(2) + C1(7)*YD(15)*YD(2))/NDM(4); %VIIa
dY(5)  = TIM*(C1(3)*YD(1)*YD(4) - C1(4)*YD(5) - C2(3)*YD(33)*YD(5)...
         - C2(7)*YD(34)*YD(5))/NDM(5); %TF:VIIa
dY(6)  = TIM*(- C1(10)*YD(5)*YD(6)/( C1(11) + YD(6) )...
         - C3(7)*((250)*YD(19))*YD(6) + C3(8)*YD(8))/NDM(6); %IX 
dY(7)  = TIM*(C1(10)*YD(5)*YD(6)/( C1(11) + YD(6) ) - C3(7)*((550)*YD(19))*YD(7)...
         + C3(8)*YD(9) - C2(5)*YD(34)*YD(7))/NDM(7); %IXa
dY(8)  = TIM*(C3(7)*((250)*YD(19))*YD(6) - C3(8)*YD(8))/NDM(8); %IX(m)
dY(9)  = TIM*(- C2(8)*YD(23)*YD(9) + C2(9)*YD(24) + C3(7)*((550)*YD(19))*YD(7)...
         - C3(8)*YD(9))/NDM(9); %IXa(m)
dY(10) = TIM*(- C1(8)*YD(5)*YD(10)/( C1(9) + YD(10) )...
         + C3(4)*YD(12)... %fX dissociation
         - C3(3)*((2700)*YD(19))*YD(10))/NDM(10); %X
%        - YD(10)*YD(19)*16000/(320*(1+(YD(10)/320)+(YD(14)/470))); % membrane binding (using K_D)  
dY(11) = TIM*(C1(8)*YD(5)*YD(10)/( C1(9) + YD(10) ) - C2(1)*YD(32)*YD(11)...
         + C2(2)*YD(33) - C2(4)*YD(34)*YD(11)+ C3(4)*YD(13)... 
         - C3(3)*((2700)*YD(19))*YD(11))/NDM(11); %Xa
         %- YD(11)*YD(19)*16000/(320*(1+(YD(10)/320)+(YD(14)/470))) % membrane binding (using K_D)...
dY(12) = TIM*(- C2(10)*YD(24)*YD(12)/( C2(11) + YD(12) ) - C3(4)*YD(12)...
         + C3(3)*((2700)*YD(19))*YD(10))/NDM(12); %X(m)
         %+ YD(10)*YD(19)*16000/(320*(1+(YD(10)/320)+(YD(14)/470))); % membrane binding (using K_D)
dY(13) = TIM*(C2(10)*YD(24)*YD(12)/( C2(11) + YD(12) ) - C2(12)*YD(13)*YD(28)...
         + C2(13)*YD(29) - C3(4)*YD(13)...
         + C3(3)*((2700)*YD(19))*YD(11))/NDM(13); %Xa(m) 
         %+ YD(11)*YD(19)*16000/(320*(1+(YD(10)/320)+(YD(14)/470))) % membrane binding (using K_D)
dY(14) = TIM*(- C1(12)*YD(11)*YD(14) + C3(6)*YD(16)...
         - C3(5)*((2000)*YD(19))*YD(14))/NDM(14); %II
%        - YD(14)*YD(19)*16000/(470*(1+(YD(10)/320)+(YD(14)/470))); % membrane binding (using K_D)  
dY(15) = TIM*(C1(12)*YD(11)*YD(14) - C2(6)*YD(34)*YD(15)...
         + C3(6)*YD(17) - C3(5)*((2000)*YD(19))*YD(15))/NDM(15); %IIa
%         - YD(15)*YD(19)*16000/(470*(1+(YD(10)/320)+(YD(14)/470))); % membrane binding (using K_D)  
dY(16) = TIM*(- C2(14)*YD(29)*YD(16)/( C2(15) + YD(16) ) - C3(6)*YD(16)...
         + C3(5)*((2000)*YD(19))*YD(14))/NDM(16); %II(m)
         %+ YD(14)*YD(19)*16000/(470*(1+(YD(10)/320)+(YD(14)/470))); % membrane binding (using K_D)
dY(17) = TIM*(C2(14)*YD(29)*YD(16)/( C2(15) +YD(16) ) - C3(6)*YD(17)...
         + C3(5)*((2000)*YD(19))*YD(15))/NDM(17); %IIa(m)
%        + YD(15)*YD(19)*16000/(470*(1+(YD(10)/320)+(YD(14)/470))); % membrane binding (using K_D)  
dY(18) = TIM*(- C3(13)*YD(18)*YD(19) - ( C3(14)*YD(18)*YD(15) )/( 1 + YD(15) ))/NDM(18); %PL 
dY(19) = TIM*(C3(13)*YD(18)*YD(19) + ( C3(14)*YD(18)*YD(15) )/( 1 + YD(15) ))/NDM(19); %AP 
dY(20) = TIM*(- C1(13)*YD(15)*YD(20)/( C1(14) + YD(20) )...
         - C3(9)*((750)*YD(19))*YD(20) + C3(10)*YD(22))/NDM(20); %VIII 
dY(21) = TIM*(C1(13)*YD(15)*YD(20)/( C1(14) + YD(20) )...
         - C3(9)*((750)*YD(19))*YD(21) + C3(10)*YD(23) - C2(18)*YD(21))/NDM(21); %VIIIa
dY(22) = TIM*(- C2(22)*YD(17)*YD(22)/( C2(23) + YD(22) )...
         - C2(16)*YD(13)*YD(22)/( C2(17) + YD(22) )...
         + C3(9)*((750)*YD(19))*YD(20) - C3(10)*YD(22))/NDM(22); %VIII(m)
dY(23) = TIM*(C2(22)*YD(17)*YD(22)/( C2(23) + YD(22) )...
         + C2(16)*YD(13)*YD(22)/( C2(17) + YD(22) )...
         + C3(9)*((750)*YD(19))*YD(21) - C3(10)*YD(23)... 
         - C2(8)*YD(23)*YD(9) + C2(9)*YD(24))/NDM(23); %VIIIa(m)
dY(24) = TIM*(C2(8)*YD(23)*YD(9) - C2(9)*YD(24))/NDM(24); %IXa:VIIIa
dY(25) = TIM*(- C1(15)*YD(15)*YD(25)/( C1(16) + YD(25) )...
         - C3(11)*((2700)*YD(19))*YD(25) + C3(12)*YD(27))/NDM(25); %V
dY(26) = TIM*(C1(15)*YD(15)*YD(25)/( C1(16) + YD(25) )...
         - C3(11)*((2700)*YD(19))*YD(26) + C3(12)*YD(28) - C2(21)*YD(26))/NDM(26); %Va
dY(27) = TIM*(- C2(24)*YD(17)*YD(27)/( C2(25) + YD(27) )...
         - C2(19)*YD(13)*YD(27)/( C2(20) + YD(27) )...
         + C3(11)*((2700)*YD(19))*YD(25) - C3(12)*YD(27))/NDM(27); %V(m)
dY(28) = TIM*(C2(24)*YD(17)*YD(27)/( C2(25) + YD(27) )...
         + C2(19)*YD(13)*YD(27)/( C2(20) + YD(27) )...
         - C2(12)*YD(13)*YD(28) + C2(13)*YD(29)...
         + C3(11)*((2700)*YD(19))*YD(26) - C3(12)*YD(28))/NDM(28); %Va(m)
dY(29) = TIM*(C2(12)*YD(13)*YD(28) - C2(13)*YD(29))/NDM(29); %Xa:Va (AM: ok, 28/8/'15)
dY(30) = TIM*(- ( C1(17)*YD(15)*YD(30) )/( C1(18) + YD(30) ))/NDM(30); %I
dY(31) = TIM*(( C1(17)*YD(15)*YD(30) )/( C1(18) + YD(30) ))/NDM(31); %Ia
dY(32) = TIM*(- C2(1)*YD(11)*YD(32) + C2(2)*YD(33))/NDM(32); %TFPI
dY(33) = TIM*(C2(1)*YD(11)*YD(32) - C2(2)*YD(33) - C2(3)*YD(5)*YD(33))/NDM(33); %Xa:TFPI
dY(34) = TIM*(- YD(34)*( (C2(4)*YD(11)) + (C2(5)*YD(7)) + (C2(6)*YD(15))...
         + (C2(7)*YD(5)) ))/NDM(34); %ATIII (AM: ok, 01/09/'15)

dY = [dY(1);dY(2);dY(3);dY(4);dY(5);dY(6);dY(7);dY(8);dY(9);dY(10);...
       dY(11);dY(12);dY(13);dY(14);dY(15);dY(16);dY(17);dY(18);dY(19);...
       dY(20);dY(21);dY(22);dY(23);dY(24);dY(25);dY(26);dY(27);dY(28);...
       dY(29);dY(30);dY(31);dY(32);dY(33);dY(34)];%./NDM)*TIM;

end